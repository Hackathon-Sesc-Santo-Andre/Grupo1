package br.org.virtualtrainersesc.model;

public class Exercicio {
	private int id;
	private String nome;
	
	public Exercicio() {
	}
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getNome() {
		return nome;
	}
	public void setNome(String nome) {
		this.nome = nome;
	}

}